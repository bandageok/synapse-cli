// Ink named colors mapped from grayscale brightness
// Map: dark (#00-#1F) → black, mid-dark (#20-#3F) → black, mid (#40-#5F) → gray,
// light-mid (#60-#7F) → gray, light (#80-#9F) → white, bright (#A0-#DF) → white, near-white (#E0-#FF) → dim

function rgbToInkColor(hex: string): string | undefined {
  const r = parseInt(hex.slice(0, 2), 16);
  const g = parseInt(hex.slice(2, 4), 16);
  const b = parseInt(hex.slice(4, 6), 16);
  const v = (r + g + b) / 3;
  if (v < 30) return 'black';
  if (v < 100) return 'black';
  if (v < 160) return 'gray';
  if (v < 220) return 'gray';
  if (v < 240) return 'white';
  if (v < 248) return 'white';
  return undefined; // near-white → transparent
}

