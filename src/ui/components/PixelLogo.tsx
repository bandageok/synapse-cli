export function PixelLogo(): React.ReactElement {
  const elements: React.ReactElement[] = [];
  for (let y = 0; y < ROWS.length; y++) {
    const row = [];
    let i = 0;
    while (i < ROWS[y].length) {
      const color = ROWS[y][i].toLowerCase();
      let count = 1;
      while (i + count < ROWS[y].length && ROWS[y][i + count].toLowerCase() === color) count++;
      if (color === 'ffffff') {
        row.push(React.createElement(Text, { key: `s-${y}-${i}` }, ' '.repeat(count)));
      } else {
        const inkColor = rgbToInkColor(color);
        row.push(React.createElement(Text, { key: `${color}-${y}-${i}`, color: inkColor as any }, '\u2588'.repeat(count)));
      }
      i += count;
    }
    elements.push(React.createElement(Box, { key: y }, ...row));
  }
  return React.createElement(Box, { flexDirection: 'column' as const, paddingLeft: 2 }, ...elements);
}
