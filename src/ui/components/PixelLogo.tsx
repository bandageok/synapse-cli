function cl(v: number): { ch: string; c: string | undefined } {
  if (v < 10) return { ch: "\u2588", c: "black" };
  if (v < 35) return { ch: "\u2588", c: "black" };
  if (v < 80) return { ch: "\u2593", c: "black" };
  if (v < 140) return { ch: "\u2592", c: "gray" };
  if (v < 200) return { ch: "\u2591", c: "gray" };
  if (v < 240) return { ch: "\u2591", c: "white" };
  return { ch: " ", c: undefined };
}

export function PixelLogo(): React.ReactElement {
  return React.createElement(Box, { flexDirection: "column" as const },
    ...G.map((row, y) => {
      const els: React.ReactElement[] = [];
      let i = 0;
      while (i < row.length) {
        const v = row[i];
        const a = cl(v);
        let n = 1;
        while (i + n < row.length) {
          const b = cl(row[i+n]);
          if (b.ch === a.ch && b.c === a.c) n++; else break;
        }
        if (a.c) els.push(React.createElement(Text, { key: y + "" + i, color: a.c as any }, a.ch.repeat(n)));
        else els.push(React.createElement(Text, { key: y + "" + i }, a.ch.repeat(n)));
        i += n;
      }
      return React.createElement(Box, { key: y }, ...els);
    }),
  );
}
