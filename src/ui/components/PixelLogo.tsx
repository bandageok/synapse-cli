function ink(v: number): string | undefined {
  return v < 100 ? "black" : v < 200 ? "gray" : v < 248 ? "white" : undefined;
}

export function PixelLogo(): React.ReactElement {
  return React.createElement(Box, { flexDirection: "column" as const, paddingLeft: 2 },
    ...G.map((row, y) => {
      const els: React.ReactElement[] = [];
      let i = 0;
      while (i < row.length) {
        const v = row[i], c = ink(v);
        let n = 1;
        while (i + n < row.length && ink(row[i + n]) === c) n++;
        if (c) {
          const ch = v < 30 ? '\u2588' : v < 80 ? '\u2593' : v < 150 ? '\u2591' : '\u2592';
          els.push(React.createElement(Text, { key: y + "-" + i, color: c as any }, ch.repeat(n)));
        } else {
          els.push(React.createElement(Text, { key: y + "-" + i }, " ".repeat(n)));
        }
        i += n;
      }
      return React.createElement(Box, { key: y }, ...els);
    }),
  );
}
